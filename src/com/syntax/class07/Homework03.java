public class Homework03 {

	public static void main(String[] args) {

		// Print even numbers from 20 to 1 (2 ways)

		int number = 20;

		while (number >= 2) {
			System.out.println(number);
			number -= 2;

		}
		System.out.println("--------01------------");

		int num = 2;

		while (num <= 20) {
			System.out.println(num);
			num += 2;
		}
	}
}
