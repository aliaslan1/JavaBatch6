package com.syntax.class02;

public class StaticMethodsQuestion {
	
	public static void main (String[] args) {
		
		System.out.println("    ------"      );
		System.out.println("  /        \\"   );
		System.out.println(" /          \\"  );
		System.out.println(" \\          /"  );
		System.out.println("  \\        /"   );		
		System.out.println("    ------"      );
		System.out.println();
		System.out.println("    ------"      );
		System.out.println("  /        \\"   );
		System.out.println(" /          \\"  );
		System.out.println("|    STOP    |"  );
		System.out.println(" \\          /"  );
		System.out.println("  \\        /"   );		
		System.out.println("    ------"      );
		
	}
	

}
