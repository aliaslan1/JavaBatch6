package com.syntax.class01;

public class PrimitiveDataType {

	public static void main(String[] args) {

		// 8 primitive data type
		// to store whole number-->byte,short,int,long

		byte by = -127;
		short s = -32768;
		int i = -2147483648;
		long l = -458875874;

		// To store decimal numbers -->float,double

		float f = 12.78f;
		double d = -9.22337236854775808;

		// to store boolean values --> boolean

		boolean mac = true;
		boolean dell = false;

		// To store single character --> char

		char container = 'F';
		char anotherVarible = '%';

		System.out.println(by);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(d);
		System.out.println(f);
		System.out.println(mac);
		System.out.println(dell);
		System.out.println(container);
		System.out.println(anotherVarible);

	}

}
