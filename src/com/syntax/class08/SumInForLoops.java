package com.syntax.class08;

public class SumInForLoops {
	
	
	  public static void main(String[] args) {

	        int sum = 0;

	        for(int i = 1; i <= 100; ++i)
	        {
	            // sum = sum + i;
	            sum += i;
	        }

	        System.out.println("Sum = " + sum);
	        
	        
	    }
      
}


